#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t			i;
	unsigned char	*p;
	unsigned char	*q;
	unsigned char	temp[len];

	if (dst == NULL || src == NULL)
		return (NULL);
	i = 0;
	p = (unsigned char *)dst;
	q = (unsigned char *)src;
	while (i < len)
	{
		temp[i] = q[i];
		i++;
	}
	i = 0;
	while (i < len)
	{
		p[i] = temp[i];
		i++;
	}
	return (dst);
}
